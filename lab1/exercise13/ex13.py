# Exercício 13 – A Força de Aceleração

# Lê os valores de espaço (E) e tempo (T)
print("Digite os valores de espaço e tempo:")
E = int(input("Espaço (E): "))
T = int(input("Tempo (T): "))

# Calcula a velocidade usando a fórmula: V = E/T
# A velocidade é um número inteiro (divisão inteira)
V = E // T

# Imprime o resultado
print(f"A velocidade alcançada é: {V}")
