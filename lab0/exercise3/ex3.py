# 3.a Multiplicação
# 20 amigos x R$35 cada
amigos_rodizio = 20
custo_por_pessoa = 35
total_conta = amigos_rodizio * custo_por_pessoa
print("O total da conta é: R$", total_conta)

# 3.b Divisão
# Conta de R$180 dividida igualmente entre 4 amigos
valor_conta = 180
amigos_restaurante = 4
valor_por_pessoa = valor_conta / amigos_restaurante
print("Cada amigo deve pagar: R$", valor_por_pessoa)

# 3.c Subtração
# Conta total R$90; Heráclito pagou R$55. Diferença que Fredegunda deve pagar.
conta_total = 90
heraclito_pagou = 55
fredegunda_deve = conta_total - heraclito_pagou
print("Fredegunda deve pagar: R$", fredegunda_deve)

# 3.d Adição
# Sosígenes gastou R$40 e Jocasta R$35. Total da conta:
sosigenes = 40
jocasta = 35
total_casal = sosigenes + jocasta
print("O total da conta deles é: R$", total_casal)


