# Exercício 7 – Número oito
# Operações diferentes que resultam no valor 8

# Adição
a_adicao = 5 + 3
print(a_adicao)

# Subtração
a_subtracao = 14 - 6
print(a_subtracao)

# Multiplicação
a_multiplicacao = 2 * 4
print(a_multiplicacao)

# Divisão (resultado exato)
a_divisao = 64 / 8  # 8.0
print(a_divisao)

# Resto da divisão (módulo)
a_resto = 26 % 9  # 26 = 2*9 (18) sobra 8
print(a_resto)

# Potenciação
a_potenciacao = 2 ** 3
print(a_potenciacao)
