# --- Exercício a) Para maiúsculas ---

# 1. Armazena a string original em uma variável.
frase_original = "Ada Lovelace foi a primeira programadora da história."

# 2. Usa o método .upper() para converter todas as letras da string para maiúsculas.
# Este método retorna uma nova string com a conversão feita.
frase_maiuscula = frase_original.upper()

# 3. Imprime a string resultante na tela.
print("String original:", frase_original)
print("String em maiúsculas:", frase_maiuscula)


# --- Exercício b) Para minúsculas ---

# 1. Armazena a string original em uma variável.
frase_original = "Augusta Ada Byron, a Condessa de Lovelace, nasceu em 1815, na Inglaterra."

# 2. Usa o método .lower() para converter todas as letras da string para minúsculas.
# Este método retorna uma nova string com todas as letras em caixa baixa.
frase_minuscula = frase_original.lower()

# 3. Imprime a string convertida.
print("String original:", frase_original)
print("String em minúsculas:", frase_minuscula)
